package com.sbellanger.arch.network.api

import javax.inject.Inject

class GithubApiFactory @Inject constructor() : BaseGithubApiFactory<IGithubApi>() {

    ///////////////////////////////////////////////////////////////////////////
    // DATA
    ///////////////////////////////////////////////////////////////////////////

    override val api: IGithubApi by lazy { buildApi() }

    ///////////////////////////////////////////////////////////////////////////
    // SPECIALIZATION
    ///////////////////////////////////////////////////////////////////////////

    override fun buildApi(): IGithubApi {
        return retrofit.create(IGithubApi::class.java)
    }
}
